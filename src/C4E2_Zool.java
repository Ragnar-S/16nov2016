public class C4E2_Zool {

    public static void zool(int value, String pet, String street) {
        System.out.println("A random value: " + value);
        System.out.println("My first pet was a " + pet);
        System.out.println("I grew up on " + street);
    }

    public static void main(String[] args) {
        zool(23, "cat", "Evergreen Terrace");       //no i did not grow up where the Simpsons live.
    }
}
